#include "libft.h"

char	*ft_strncpy(char *str1, const char *str2, size_t n)
{
	char	*c1;

	c1 = str1;
	while (*c1)
		c1++;
	while (*str2 && n > 0)
	{
		*c1 = *str2;
		c1++;
		str2++;
		n--;
	}
	*c1 = '\0';
	return (str1);
}
